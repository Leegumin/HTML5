public class IntelliJ {
    // 단축키 : main + 빠른완성
    public static void main(String[] args) {
        // 빌드 + 실행 단축키 : CTRL + SHIFT + F10
        // 단축키 : sout + 빠른완성
        System.out.println("Hello IntelliJ");
        // 단축키 : souf + 빠른완성
        System.out.printf("Hello IntelliJ");
        // 쉬프트*2 -> 검색창
        // Alt+1 -> 프로젝트창으로 이동
        // ESC -> 에디드창으로 복귀
    }
}
