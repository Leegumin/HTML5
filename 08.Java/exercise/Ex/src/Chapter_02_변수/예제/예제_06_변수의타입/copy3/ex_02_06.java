package Chapter_02_변수.예제.예제_06_변수의타입.copy3;

public class ex_02_06 {
	public static void main(String args[]) {
		/* 정수를 저장할 변수의 타입 */		
		int x = 100;
		/* 실수를 저장할 변수의 타입 */		
		double pi = 3.14;
		/* 문자 1개를 저장할 변수의 타입("" 안됨 무조건 '') */
		char ch = 'a';
		/* 여러문자를 저장할 변수의 타입('' 안됨 무조건 "") */
		String str = "abc";
		
		System.out.println(x);
		System.out.println(pi);
		System.out.println(ch);
		System.out.println(str);
	}
}
